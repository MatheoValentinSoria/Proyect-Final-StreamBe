DROP DATABASE IF EXISTS wb_turnos ;

CREATE DATABASE wb_turnos;
USE wb_turnos;

CREATE TABLE empresa (
	id INT AUTO_INCREMENT NOT NULL,
    nombre VARCHAR(255) NOT NULL,
    rep_legal VARCHAR(255) NULL,
    gerente VARCHAR(255) NULL,
    direccion VARCHAR(255) NULL,
    telefono BIGINT(10) NULL,
	zip INT(6) NULL,
    PRIMARY KEY (id)
);

CREATE TABLE acceso (
	_username_ VARCHAR(255) NOT NULL DEFAULT 'admin',
    _password_ VARCHAR(255) NOT NULL DEFAULT 'admin',
	ID INT NOT NULL,
    PRIMARY KEY (_username_),
    CONSTRAINT fk_credencialesID
    FOREIGN KEY (ID) REFERENCES empresa(id)
    ON DELETE CASCADE
    ON UPDATE CASCADE
);

CREATE TABLE empleado (
	id INT AUTO_INCREMENT NOT NULL,
    EMPRESA INT NOT NULL,
    nombres VARCHAR(255) NOT NULL,
    apellido1 VARCHAR(255) NOT NULL,
    apellido2 VARCHAR(255) NULL,
    celular BIGINT(10) NULL,
    cargo VARCHAR(255) NOT NULL DEFAULT 'cajero',
    PRIMARY KEY (id),
    CONSTRAINT fk_empleadoEMPRESA
    FOREIGN KEY (EMPRESA) REFERENCES empresa(id)
    ON DELETE CASCADE
    ON UPDATE CASCADE
);

CREATE TABLE turno (
	id BIGINT AUTO_INCREMENT NOT NULL,
    t_documento VARCHAR(2) NOT NULL,
    documento BIGINT(10) NOT NULL,
    letra VARCHAR(1) NOT NULL,
    numero INT(3) NOT NULL,
    t_atencion VARCHAR(255) NOT NULL,
    estado VARCHAR(10) NOT NULL,
    fecha TIMESTAMP NULL DEFAULT '0000-00-00 00:00:00',
    PRIMARY KEY (id)
);

